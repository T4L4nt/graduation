#!/usr/bin/env python
"""
Phase 9 Task 4: Bootstrap confidence intervals for structural distance matrix.

Resamples images (with replacement) within each architecture, recomputes the
mean drift profile → structural features → pairwise distance matrix, and
extracts 95% CI for each pairwise distance.

Uses per-image drift data from phase1 (SD 1.5), SDXL, DiT, SD 3.5, and
DiT-S/2 controlled experiment. FLUX has no per-image data → uses parametric
bootstrap from aggregated mean±std (less ideal, flagged).

Usage:
    python scripts/phase9_task4_bootstrap.py

Output: outputs/phase9_task4/
"""

import json
import sys
from pathlib import Path

import numpy as np

PROJECT_ROOT = Path(__file__).resolve().parent.parent
OUT_DIR = PROJECT_ROOT / "outputs" / "phase9_task4"
OUT_DIR.mkdir(parents=True, exist_ok=True)
N_BOOTSTRAP = 10000


# ---------------------------------------------------------------------------
# Structural features
# ---------------------------------------------------------------------------

def extract_structural_features(drift: np.ndarray) -> np.ndarray:
    L = len(drift)
    d = np.maximum(drift, 0.0)
    d_max = d.max()
    if d_max < 1e-12:
        return np.array([0.5, 0.0, 0.0, 0.0])
    peak_pos = float(np.argmax(d)) / max(L - 1, 1)
    n_peaks = float(np.sum(d > 0.7 * d_max))
    d_sorted = np.sort(d)
    n = len(d_sorted)
    index = np.arange(1, n + 1)
    gini = (2.0 * np.sum(index * d_sorted)) / (n * np.sum(d_sorted) + 1e-12) - (n + 1) / n
    spread = float(np.std(d) / (np.mean(d) + 1e-12))
    return np.array([peak_pos, n_peaks, gini, spread])


def structural_distance(d1: np.ndarray, d2: np.ndarray) -> float:
    return float(np.linalg.norm(extract_structural_features(d1) - extract_structural_features(d2)))


# ---------------------------------------------------------------------------
# Data loaders: return (layer_names, per_image_matrix: [n_layers, n_images])
# ---------------------------------------------------------------------------

def load_per_image_sd15():
    """SD 1.5 per-image drift. Generated by phase9_task4_sd15_perimage.py.
    Format: {layer_name: {image_name: drift_value}} (19 images, 38 layers)."""
    p = PROJECT_ROOT / "outputs" / "phase9_task4" / "sd15_per_image_drift.json"
    if not p.exists():
        print("  SD 1.5: per-image data not found — run phase9_task4_sd15_perimage.py first")
        return None, None, None
    with open(p) as f:
        data = json.load(f)
    per_img = data["per_image"]
    # Format: {layer_name: {image_name: drift_value}}
    layer_names = sorted(per_img.keys())
    first_layer = per_img[layer_names[0]]
    img_names = sorted(first_layer.keys())
    mat = np.array([[per_img[l][img] for img in img_names] for l in layer_names])
    return layer_names, mat, img_names


